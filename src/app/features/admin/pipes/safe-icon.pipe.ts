@Pipe({
  name: 'safeIcon',
  pure: true,
})
export class SafeIconPipe implements PipeTransform {
  // ANG20-02: inject() function instead of constructor injection
  private readonly sanitizer = inject(DomSanitizer);

  transform(iconName: string): SafeHtml {
    const svg = ICON_REGISTRY[iconName];
    if (!svg) {
      // CQ-07: Unknown icon — return empty instead of console.warn
      return '';
    }
    return this.sanitizer.bypassSecurityTrustHtml(svg);
  }
}
